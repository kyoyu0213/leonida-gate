const DICTS: Record<Lang, Dict> = { ja: JA, en: EN };

/** 翻訳関数を返すフック。t('home.latest') のように使う。未定義キーは日本語→キー名でフォールバック。 */
export function useT(): (key: string) => string {
  const lang = useLang();
  return (key: string) => DICTS[lang][key] ?? JA[key] ?? key;
}
